package com.bubblesLaundry.bean;

public class StoreBean {
	private int s_id;
	private String s_location;
	private String s_address;
	private String s_timings;

	/**
	 * @return the s_id
	 */
	public int getS_id() {
		return s_id;
	}

	/**
	 * @param s_id the s_id to set
	 */
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}

	/**
	 * @return the s_location
	 */
	public String getS_location() {
		return s_location;
	}

	/**
	 * @param s_location the s_location to set
	 */
	public void setS_location(String s_location) {
		this.s_location = s_location;
	}

	/**
	 * @return the s_address
	 */
	public String getS_address() {
		return s_address;
	}

	/**
	 * @param s_address the s_address to set
	 */
	public void setS_address(String s_address) {
		this.s_address = s_address;
	}

	/**
	 * @return the s_timings
	 */
	public String getS_timings() {
		return s_timings;
	}

	/**
	 * @param s_timings the s_timings to set
	 */
	public void setS_timings(String s_timings) {
		this.s_timings = s_timings;
	}

}
